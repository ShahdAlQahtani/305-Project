            String query = "Select `Hallname` , `HallPrice`  from `hallinfo`,`favorites` where `favorites`.`idTenant` ='" + Login.Id + "' and `favorites`.`idHallInfo` ='" + TenantPage_Hallinfo.id + "' and `hallinfo`.`idHallInfo`=`favorites`.`idHallInfo` ";  
